export type MapLabelKind =
  | 'continent'
  | 'ocean'
  | 'country'
  | 'region'
  | 'city'
  | 'china-city'
  | 'district'
  | 'attack-city'
  | 'target-city'

export interface ChineseMapLabel {
  text: string
  latitude: number
  longitude: number
  kind: MapLabelKind
  minZoom: number
  maxZoom: number
  priority: number
  offsetX?: number
  offsetY?: number
}

type LabelSeed = readonly [
  text: string,
  latitude: number,
  longitude: number,
  minZoom: number,
  maxZoom: number,
  priority?: number,
]

function createLabels(
  kind: MapLabelKind,
  defaultPriority: number,
  seeds: readonly LabelSeed[],
): ChineseMapLabel[] {
  return seeds.map(([text, latitude, longitude, minZoom, maxZoom, priority]) => ({
    text,
    latitude,
    longitude,
    kind,
    minZoom,
    maxZoom,
    priority: priority ?? defaultPriority,
  }))
}

